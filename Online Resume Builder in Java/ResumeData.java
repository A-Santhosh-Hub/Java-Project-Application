public class ResumeData {
    String name, email, phone, summary, education, experience, skills;

    public ResumeData(String name, String email, String phone,
                      String summary, String education, String experience, String skills) {
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.summary = summary;
        this.education = education;
        this.experience = experience;
        this.skills = skills;
    }
}
